// 문자열 연결 연산자
console.log('1' + 2);
console.log(1 + '2');

// 산술 연산자
console.log(1 + 2);

// true 는 1로 타입 변환된다
console.log(1 + true);

// false 는 0으로 타입 변환된다
console.log(1 + false);

// null 은 0으로 타입 변환된다
console.log(1 + null);

// undefined는 숫자로 타입 변환되지 않는다
console.log(+undefined);
console.log(1 + undefined);