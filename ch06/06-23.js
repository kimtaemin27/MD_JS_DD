// undefined
var foo;
console.log(typeof foo);

// number
foo = 3;
console.log(typeof foo);

// string
foo = 'Hello';
console.log(typeof foo);

// boolean
foo = true;
console.log(typeof foo);

// object
foo = null;
console.log(typeof foo);

// symbol
foo = Symbol();
console.log(typeof foo);

// object
foo = {};
console.log(typeof foo);

// object 
foo = [];
console.log(typeof foo);

// function
foo = function() {};
console.log(typeof foo);